package crossword.logic;

/**
 *
 * @author Vanamo Piirainen
 */
public class LetterPosition {
    
    private int x;
    private int y;
    
    public LetterPosition(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
